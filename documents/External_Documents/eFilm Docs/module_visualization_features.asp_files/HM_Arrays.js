function MenuPos_Left(strMenuName)
{
	if (is.ie)
	{
		var oMenu = document.all(strMenuName)
		var nReturn = FindPageOffset(oMenu, 'Left') ;
	}
	else
	{
		if (is.ns5)
		{
			var oImg = eval("document." + strMenuName);
			var nReturn = oImg.offsetLeft ;
		}
		else
		{
			var oImg = eval("document." + strMenuName);
			var nReturn = oImg.x;
		}
	}

	return nReturn ;
}

function MenuPos_Top(strMenuName, strArrayNo)
{
	if (is.ie)
	{
		var oImg = document.all(strMenuName)
		var nReturn = FindPageOffset(oImg, 'Top') ;
		
		var oMenu = document.all('HM_Menu' + strArrayNo) ;
		
		if (oMenu)
		{
			if (nReturn + oMenu.offsetHeight > document.body.scrollHeight)
				nReturn = nReturn - oMenu.offsetHeight - 25;
		}
	}
	else
	{
		if (is.ns5)
		{
			var oImg = eval("document." + strMenuName);
			var nReturn = oImg.offsetTop ;
		}
		else
		{
			var oImg = eval("document." + strMenuName);
			var nReturn = oImg.y;

			var oMenu = eval("window.HM_Menu" + strArrayNo);
			
			if (oMenu)
			{
				if (oMenu.clip.height + nReturn > window.innerHeight)
					nReturn = nReturn - oMenu.clip.height - 25 ;
			}
		}
	}

	return nReturn ;
}

		function FindPageOffset
		(
			oElement,
			sTopOrLeft
		)
		{
		
			if (is.ie && ( sTopOrLeft =="Left" || sTopOrLeft =="Top" ) )
			{
				// if the element has a offset parent get the parent's offset
				if (oElement.offsetParent)
				{
					return eval("oElement.offset" +sTopOrLeft) + FindPageOffset( oElement.offsetParent, sTopOrLeft );
				}
				else
				{
					return eval( "oElement.offset" + sTopOrLeft );
				}
			}
			else
			{
				return 0;
			}
		};

function ShowSideMenuNo(strNo)
{
	if (is.ie)
	{
		var oMenu = document.all("SideMenuNo") ;
	}
	else
	{
		var oMenu = eval("document.frmSideMenu.SideMenuNo");
	}

	if (oMenu.value == strNo)
		return "1" ;
	else
		return "0" ;

}




